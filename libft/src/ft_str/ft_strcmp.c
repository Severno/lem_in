#include "../../includes/libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;
	int	compare;

	i = 0;
	compare = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	if (s1[i] != s2[i])
		compare = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (compare);
}
