#include "../../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*s2;

	if (!(s2 = ft_strnew(ft_strlen(s1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	return (s2);
}
