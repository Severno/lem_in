#include "../../includes/libft.h"

int		ft_str_is_printable(char *str)
{
	while (*str)
	{
		if ((*str >= 1 && *str <= 31) || *str == 127 || *str == '\xf0')
			return (0);
		str++;
	}
	return (1);
}
